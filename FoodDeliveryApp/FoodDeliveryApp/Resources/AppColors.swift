import UIKit

enum AppColors {
    static let white = UIColor().hex(0xFFFFFF)
    static let black = UIColor().hex(0x000000)
    static let lightBlack = UIColor().hex(0x000000).withAlphaComponent(0.5)
    static let gray = UIColor().hex(0xECF0F1)
    static let darkGray = UIColor().hex(0x34495E)
    static let halfDarkGray = UIColor().hex(0x34495E).withAlphaComponent(0.3)
    static let myOrange = UIColor().hex(0xD35400)
}
